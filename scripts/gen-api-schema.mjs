// Regenerates docs/mineflayer/api-schema.md — a condensed-but-complete signature index of the
// mineflayer + pathfinder API — from the vendored docs (docs/mineflayer/*.md). Run after
// bumping mineflayer/pathfinder and re-copying their docs:  node scripts/gen-api-schema.mjs
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const SKIP = new Set([
  'Install', 'Tutorial & Explanation', 'Video Tutorials', 'Example', 'Features',
  'Usage', 'API', 'Mineflayer-pathfinder', 'Example:',
]);

/** Extract "signature — first description line" entries grouped by section header, dropping
 *  any section that ends up with no entries and any prose-only section in SKIP. */
function condense(path, entryMarks, sectionMarks) {
  const lines = readFileSync(path, 'utf8').split(/\r?\n/);
  const sections = [];
  let cur = null;
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const sec = sectionMarks.find((m) => line.startsWith(m + ' '));
    if (sec) {
      cur = { title: line.slice(sec.length).trim(), entries: [] };
      sections.push(cur);
      continue;
    }
    const mark = entryMarks.find((m) => line.startsWith(m + ' '));
    if (!mark) continue;
    const sig = line.slice(mark.length).trim();
    let desc = '';
    for (let j = i + 1; j < lines.length && j < i + 8; j++) {
      const t = lines[j].trim();
      if (!t) continue;
      if (/^#/.test(t)) break;
      desc = t.replace(/`/g, '').replace(/\[([^\]]+)\]\([^)]+\)/g, '$1');
      break;
    }
    if (desc.length > 140) desc = desc.slice(0, 137) + '...';
    if (!cur) {
      cur = { title: '', entries: [] };
      sections.push(cur);
    }
    cur.entries.push(desc ? `- ${sig} — ${desc}` : `- ${sig}`);
  }
  return sections
    .filter((s) => s.entries.length && !SKIP.has(s.title))
    .map((s) => `\n### ${s.title}\n${s.entries.join('\n')}`)
    .join('\n');
}

const mf = condense(`${ROOT}/docs/mineflayer/mineflayer-api.md`, ['####'], ['##', '###']);
const pf = condense(`${ROOT}/docs/mineflayer/mineflayer-pathfinder-api.md`, ['###'], ['#', '##']);

const header = `# Mineflayer API schema (condensed — complete surface)
Auto-generated by scripts/gen-api-schema.mjs from the vendored docs (mineflayer 4.37.1 /
pathfinder 2.4.5). Every method, property, and event signature is listed so sandbox code can
use the full API without guessing. Promises must be awaited. Positions are Vec3.
mcData is minecraft-data for this version (mcData.blocksByName / mcData.itemsByName / ...).`;

const full = `${header}\n\n# MINEFLAYER (bot.*)\n${mf}\n\n# PATHFINDER (bot.pathfinder.*)\n${pf}\n`;
writeFileSync(`${ROOT}/docs/mineflayer/api-schema.md`, full);
console.log(`Wrote docs/mineflayer/api-schema.md (${full.split('\n').length} lines, ~${Math.round(full.length / 4)} tokens).`);
